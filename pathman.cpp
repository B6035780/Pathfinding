template<typename T, typename priority_t>
struct PriorityQueue
{
	typedef std::pair<priority_t, T> PQElement;
	std::priority_queue<PQElement, std::vector<PQElement>, std::greater<PQElement>> elements;

	inline bool empty() const
	{
		return elements.empty();
	}

	inline void put(T item, priority_t priority)
	{
		elements.emplace(priority, item);
	}

	T get()
	{
		T best_item = elements.top().second;
		elements.pop();
		return best_item;
	}
};

struct Location
{
	Location() : x(0), y(0) {};
	Location(int x, int y) : x(x), y(y) {};
	int x, y;

	bool operator==(const Location& rh) const
	{
		return x == rh.x && y == rh.y;
	}

	bool operator<(const Location& rh) const
	{
		return std::tie(x, y) < std::tie(rh.x, rh.y);
	}
};

namespace std {
	/* implement hash function so we can put GridLocation into an unordered_set */
	template <> struct hash<Location> {
		typedef Location argument_type;
		typedef std::size_t result_type;
		std::size_t operator()(const Location& id) const noexcept {
			return std::hash<int>()(id.x ^ (id.y << 4));
		}
	};
}


std::vector<Location> neighbours(Location id) 
{
	std::vector<Location> results;
	std::vector<Location> DIRS;

	Location N(0, -1);
	Location E(1, 0);
	Location S(0, 1);
	Location W(-1, 0);

	switch (tile_map[id.x + (id.y * tile_map_width)])	//Checks which directions pathman can move based on the flag in tilemap
	{
	case 10:
		DIRS.push_back(S);
		DIRS.push_back(E);
		break;
	case 11:
		DIRS.push_back(S);
		DIRS.push_back(N);
		DIRS.push_back(E);
		break;
	case 12:
		DIRS.push_back(E);
		DIRS.push_back(W);
		break;
	case 13:
		DIRS.push_back(N);
		DIRS.push_back(E);
		DIRS.push_back(W);
		break;
	case 14:
		DIRS.push_back(S);
		DIRS.push_back(E);
		DIRS.push_back(W);
		break;
	case 15:
		DIRS.push_back(N);
		DIRS.push_back(E);
		DIRS.push_back(S);
		DIRS.push_back(W);
		break;
	case 3:
		DIRS.push_back(S);
		DIRS.push_back(N);
		break;
	case 5:
		DIRS.push_back(N);
		DIRS.push_back(W);
		break;
	case 6:
		DIRS.push_back(S);
		DIRS.push_back(W);
		break;
	case 7:
		DIRS.push_back(N);
		DIRS.push_back(S);
		DIRS.push_back(W);
		break;
	case 9:
		DIRS.push_back(N);
		DIRS.push_back(E);
		break;
	default:
		assert(false);
		break;
	}

	for (Location dir : DIRS)
	{
		Location next(id.x + dir.x, id.y + dir.y);
		results.push_back(next);
	}

	if ((id.x + id.y) % 2 == 0) {
		//aesthetic improvement on square grids
		std::reverse(results.begin(), results.end());
	}

	return results;
}

double heuristic(Location next, Location goal)	//for a* search
{
	return std::abs(next.x - goal.x) + std::abs(next.y - goal.y);
}

double cost(Location current, Location next)	//We don't have different terrain types, and walls can't be moved to, so cost is always 0
{
	return 0.0;
}

void breadthFirstSearch(Location start, Location goal, std::unordered_map<Location, Location>& cameFrom)
{
	std::queue<Location> frontier;
	frontier.push(start);

	cameFrom[start] = start;

	while (!frontier.empty()) 
	{
		Location current = frontier.front();
		frontier.pop();

		if (current == goal) 
			break;
		

		for (Location next : neighbours(current)) 
		{
			if (cameFrom.find(next) == cameFrom.end())
			{
				frontier.push(next);
				cameFrom[next] = current;
			}
		}
	}
}

void djikstraSearch(Location start, Location goal, std::unordered_map<Location, Location>& cameFrom,
	std::unordered_map<Location, double>& costSoFar)
{
	PriorityQueue<Location, double> frontier;
	frontier.put(start, 0.0);

	cameFrom[start] = start;
	costSoFar[start] = 0.0;

	while (!frontier.empty())
	{
		Location current = frontier.get();

		if (current == goal)
			break;

		for (Location next : neighbours(current))
		{
			double newCost = costSoFar[current] + cost(current, next);

			if (costSoFar.find(next) == costSoFar.end()
				|| newCost < costSoFar[next])
			{
				costSoFar[next] = newCost;
				double prio = newCost;
				frontier.put(next, prio);
				cameFrom[next] = current;
			}
		}
	}

}

void aStarSearch(Location start, Location goal, std::unordered_map<Location, Location>& cameFrom,	//Same as djikstra, minus the heuristic
	std::unordered_map<Location, double>& costSoFar)
{
	//std::priority_queue<Location, double, std::greater<double>> frontier;
	PriorityQueue<Location, double> frontier;
	frontier.put(start, 0.0);

	cameFrom[start] = start;
	costSoFar[start] = 0.0;

	while (!frontier.empty())
	{
		Location current = frontier.get();

		if (current == goal)
			break;

		for (Location next : neighbours(current))
		{
			double newCost = costSoFar[current] + cost(current, next);
			
			if (costSoFar.find(next) == costSoFar.end()
				|| newCost < costSoFar[next])
			{
				costSoFar[next] = newCost;
				double prio = newCost + heuristic(next, goal);
				frontier.put(next, prio);
				cameFrom[next] = current;
			}
		}
	}
}

